    public static int getId(final String identifier, final int def) {
        int id = IDS.getOrDefault(Key.stripMinecraftNamespace(identifier), -1);
        if (id == -1) {
            try {
                final int parsedId = Integer.parseInt(identifier);
                if (IDS.containsValue(parsedId)) {
                    id = parsedId;
                }
            } catch (final NumberFormatException ignored) {
            }
        }
        return id != -1 ? id : def;
    }

